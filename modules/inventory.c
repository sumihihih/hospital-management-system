void searchInventoryByID(void)
{
    FILE *file;
    Inventory inv;
    char searchID[10];
    char itemIdLower[10];
    int found;

    file = fopen("data/inventory.txt", "r");

    if (file == NULL)
    {
        printf("Error opening inventory file.\n");
        return;
    }

    found = 0;

    printf("\nEnter Item ID: ");
    scanf(" %9[^\n]", searchID);
    while (getchar() != '\n');

    toLowerCase(searchID);
    fscanf(file, "%*[^\n]\n");

    while (fscanf(file, " %9[^|]|%49[^|]|%29[^|]|%d",
                  inv.item_id, inv.item_name, inv.category, &inv.stock_level) == 4)
    {
        strcpy(itemIdLower, inv.item_id);
        toLowerCase(itemIdLower);

        if (strcmp(searchID, itemIdLower) == 0)
        {
            printInventoryDetails(inv);
            found = 1;
            break;
        }
    }

    fclose(file);

    if (found == 0)
    {
        printf("Item not found.\n");
    }

    waitForEnter();
}

