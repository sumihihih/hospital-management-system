static void printInventoryTableHeader(const char title[])
{
    printf("\n============================================================\n");
    printf("%28s\n", title);
    printf("============================================================\n");
    printf("%-8s %-20s %-12s %-8s\n", "ItemID", "ItemName", "Category", "Stock");
    printf("------------------------------------------------------------\n");
}

static void printInventoryRow(Inventory inv)
{
    printf("%-8s %-20s %-12s %-8d\n",
           inv.item_id, inv.item_name, inv.category, inv.stock_level);
}

static void printInventoryDetails(Inventory inv)
{
    printf("\nItem Found:\n");
    printf("Item ID     : %s\n", inv.item_id);
    printf("Item Name   : %s\n", inv.item_name);
    printf("Category    : %s\n", inv.category);
    printf("Stock Level : %d\n", inv.stock_level);
}

static void waitForEnter(void)
{
    printf("\nPress Enter to go back...");
    getchar();
}

static void getInventoryReportDateTime(char dateTime[])
{
    time_t t;
    struct tm *currentTime;

    t = time(NULL);
    currentTime = localtime(&t);

    strftime(dateTime, 50, "%Y-%m-%d %H:%M:%S", currentTime);
}

int chooseInventoryCategory(char category[])
{
    int categoryChoice;

    while (1)
    {
        printf("\nChoose Category:\n");
        printf("1. Medication\n");
        printf("2. Supplies\n");
        printf("3. Equipment\n");
        printf("Enter choice: ");

        if (scanf("%d", &categoryChoice) != 1)
        {
            printf("Invalid input. Please enter a value from 1 to 3.\n");
            while (getchar() != '\n');
            continue;
        }

        while (getchar() != '\n');

        if (categoryChoice == 1)
        {
            strcpy(category, "Medication");
            return categoryChoice;
        }
        else if (categoryChoice == 2)
        {
            strcpy(category, "Supplies");
            return categoryChoice;
        }
        else if (categoryChoice == 3)
        {
            strcpy(category, "Equipment");
            return categoryChoice;
        }

        printf("Invalid choice. Please try again.\n");
    }
}

