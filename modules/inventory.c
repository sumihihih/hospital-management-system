void filterInventoryByStockLevel(char currentUserName[])
{
    FILE *file;
    Inventory inv;
    char stockLabel[20];
    char dateTime[50];
    int choice;
    int minStock;
    int maxStock;
    int found;

    while (1)
    {
        printf("\nChoose Stock Level:\n");
        printf("1. Out of Stock (0)\n");
        printf("2. Low Stock (1-20)\n");
        printf("3. Stable (21-100)\n");
        printf("4. Surplus (more than 100)\n");
        printf("Enter choice: ");

        if (scanf("%d", &choice) != 1)
        {
            printf("Invalid input. Please enter 1-4.\n");
            while (getchar() != '\n');
            continue;
        }

        while (getchar() != '\n');

        if (choice == 1)
        {
            minStock = 0;
            maxStock = 0;
            strcpy(stockLabel, "Out of Stock");
            break;
        }
        else if (choice == 2)
        {
            minStock = 1;
            maxStock = 20;
            strcpy(stockLabel, "Low Stock");
            break;
        }
        else if (choice == 3)
        {
            minStock = 21;
            maxStock = 100;
            strcpy(stockLabel, "Stable");
            break;
        }
        else if (choice == 4)
        {
            minStock = 101;
            maxStock = 2147483647;
            strcpy(stockLabel, "Surplus");
            break;
        }

        printf("Invalid choice. Please enter 1-4.\n");
    }

    file = fopen("data/inventory.txt", "r");

    if (file == NULL)
    {
        printf("Error opening inventory file.\n");
        return;
    }

    found = 0;
    getInventoryReportDateTime(dateTime);

    printf("\n=====================================\n");
    printf("       FILTERED INVENTORY REPORT     \n");
    printf("=====================================\n");
    printf("Report generated by %s at %s\n", currentUserName, dateTime);
    printf("Filtered By Stock Level: %s\n", stockLabel);
    printf("------------------------------------------------------------\n");
    printf("%-8s %-20s %-12s %-8s\n", "ItemID", "ItemName", "Category", "Stock");
    printf("------------------------------------------------------------\n");

    fscanf(file, "%*[^\n]\n");

    while (fscanf(file, " %9[^|]|%49[^|]|%29[^|]|%d",
                  inv.item_id, inv.item_name, inv.category, &inv.stock_level) == 4)
    {
        if (inv.stock_level >= minStock && inv.stock_level <= maxStock)
        {
            printInventoryRow(inv);
            found = 1;
        }
    }

    fclose(file);

    if (found == 0)
    {
        printf("No items found in this stock range.\n");
    }

    waitForEnter();
}
